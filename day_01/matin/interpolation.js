let a = 51; // int
let b = 90; // int
// (a + b) sont isolés dans des parenthèses pour bien additionner ces 2 integer et les concatener au reste de la sting
// \n = saut de ligne
console.log("Somme " + (a + b) + " et\n multiplication " + a * b + ".");

// une string s'écroit soit entre "" (double quotes), soit entre '' (simple quotes), ou soit entre `` (backquotes)
console.log(`Somme : ${a + b} et \n multiplication : ${a * b}.`);

